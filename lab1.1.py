a = int(input("Введіть значення а: "))
while (a < 1 or a > 100) :
  a = int(input("Введіть значення 'а' в межах від 1 до 100, ще раз: "))
b = int(input("Введіть значення b: "))
while (b < 1 or a > 100) :
  b = int(input("Введіть значення 'b' в межах від 1 до 100, ще раз: "))
if a < b :
  r = (a - b) / a
elif a == b :
  r = -2
else:
  r = a / b + 1
print("Результат обчислення виразу: ", round(r, 3))
